// Viết hàm tại đây
function getLastElement(array){
    return array[0];
}
// Ví dụ sử dụng
var animals = ['Monkey', 'Tiger', 'Elephant','a ','b ','c '];
var result = getLastElement(animals);

console.log(result); // Expected: "Elephant"
console.log(animals); // Expected: ['Monkey', 'Tiger', 'Elephant']